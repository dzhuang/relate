def generate_random_tree():
    for i in range(N_TREE):
        r.clipboard_append("#%d\n" % i)
        nodes = [x for x in range(int(N_NODE))]
        g = partition(nodes, N_EDGE)
        for matrix in g.as_np_matrix_list(weight_random_range=WEIGHT_RANGE, number_of_matrix=10):
            for line in generate_matrix_str(matrix, i):
                r.clipboard_append(line)
                r.clipboard_append("\n")


